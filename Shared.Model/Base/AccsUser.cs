                SessionId = Guid.NewGuid().ToString(),
                MacAddress = macAddress,
                UserAgent = userAgent,
                IpAddress = ipAddress,
                LoginTime = DateTime.UtcNow,
                LastActivity = DateTime.UtcNow,
                DeviceName = deviceName,
                IsActive = true
            });
        }

